package com.ds.common.bet;

/**
 * 默认限额
 * 
 * @author jackson
 *
 */
public enum BetLimitValue {

	MIN(1, "min"),

	MAX(10000, "max");

	private Integer limit;
	private String name;

	public Integer getLimit() {
		return limit;
	}

	public String getName() {
		return name;
	}

	private BetLimitValue(Integer limit, String name) {
		this.limit = limit;
		this.name = name;
	}

}
