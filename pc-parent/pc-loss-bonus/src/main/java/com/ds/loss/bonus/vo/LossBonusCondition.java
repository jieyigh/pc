package com.ds.loss.bonus.vo;

import java.io.Serializable;
import java.math.BigDecimal;

/**
 * 亏损送条件vo
 * 
 * @author jackson
 *
 */
public class LossBonusCondition implements Serializable {

	private static final long serialVersionUID = 7561846104136091878L;

	private BigDecimal lossMoney;
	private BigDecimal bonusPercent;

	public BigDecimal getLossMoney() {
		return lossMoney;
	}

	public void setLossMoney(BigDecimal lossMoney) {
		this.lossMoney = lossMoney;
	}

	public BigDecimal getBonusPercent() {
		return bonusPercent;
	}

	public void setBonusPercent(BigDecimal bonusPercent) {
		this.bonusPercent = bonusPercent;
	}

}
