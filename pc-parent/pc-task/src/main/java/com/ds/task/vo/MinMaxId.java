package com.ds.task.vo;

/**
 * 统计最小最大id
 * 
 * @author jackson
 *
 */
public class MinMaxId {

	private Long minId;
	private Long maxId;

	public Long getMinId() {
		return minId;
	}

	public void setMinId(Long minId) {
		this.minId = minId;
	}

	public Long getMaxId() {
		return maxId;
	}

	public void setMaxId(Long maxId) {
		this.maxId = maxId;
	}

}
